/**
 * Get an icon by name
 * @param {string} name - Icon name
 * @param {Object} options - Icon options (size, color, class)
 * @returns {string} HTML string with SVG
 */
export function getIcon(name, options = {}) {
  const {
    size = 20,
    color = 'currentColor',
    className = '',
  } = options;

  const icon = icons[name];
  if (!icon) {
    console.warn(`[PerspectiveLens] Icon not found: ${name}`);
    return icons.info; // Fallback icon
  }

  // Replace default size and color
  let modifiedIcon = icon
    .replace(/width="\d+"/, `width="${size}"`)
    .replace(/height="\d+"/, `height="${size}"`);

  // Add class if provided
  if (className) {
    modifiedIcon = modifiedIcon.replace('<svg', `<svg class="${className}"`);
  }

  return modifiedIcon;
}

/**
 * Create an icon element
 * @param {string} name - Icon name
 * @param {Object} options - Icon options
 * @returns {HTMLElement} SVG element
 */
export function createIcon(name, options = {}) {
  const wrapper = document.createElement('span');
  wrapper.innerHTML = getIcon(name, options);
  wrapper.style.display = 'inline-flex';
  wrapper.style.alignItems = 'center';
  wrapper.style.justifyContent = 'center';
  return wrapper.firstChild;
}

